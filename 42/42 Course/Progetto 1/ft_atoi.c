int	ft_atoi(char *str)
{
	int	n;
	int	s;
	int	i;

	n = 0;
	s = 1;
	i = 0;
	while (*(str + i) == ' ' || (*(str + i) >= 9 && *(str + i) <= 13))
		i++;
	while (*(str + i) == '-' || *(str + i) == '+')
	{
		if (*(str + i) == '-')
			s *= -1;
		i++;
	}
	while (*(str + i) >= '0' && *(str + i) <= '9')
	{
		n *= 10;
		n += (int)(*(str + i) - '0');
		i++;
	}
	return (n * s);
}
